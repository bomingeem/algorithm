package study.baekjoon.step3;

import java.util.Scanner;

public class boj2742 {
    public static void main(String[] args) {
        //[백준] 기찍 N
        Scanner scanner = new Scanner(System.in);
        int N = scanner.nextInt();

        for (int i=N; i>0; i--) {
            System.out.println(i);
        }
    }
}
